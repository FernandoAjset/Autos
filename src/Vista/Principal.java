    public void fondo() {
        //Inicializar imagen
        Image imagen = null;
        try {
            //Inicializar y asignar url de la imagen a mostrar
            URL url = new URL("https://i.imgur.com/F2uEALy.jpg");
            //Asignacion de url a la imagen
            imagen = ImageIO.read(url);
        } catch (IOException e) {
            e.printStackTrace();
        }
        //Mostrar imagen
        jLabelFondo.setIcon(new ImageIcon(imagen));
    }

